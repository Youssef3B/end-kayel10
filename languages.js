// Function to set the selected language in localStorage
function setSelectedLanguage(language) {
  localStorage.setItem("selectedLanguage", language);
}

// Function to get the selected language from localStorage, defaulting to 'en'
function getSelectedLanguage() {
  return localStorage.getItem("selectedLanguage") || "en";
}

// Function to update content based on the selected language
function updateContent() {
  const selectedLanguage = getSelectedLanguage();
  const elements = document.querySelectorAll("[data-translation-key]");

  elements.forEach((element) => {
    const key = element.getAttribute("data-translation-key");
    element.textContent = translations[selectedLanguage][key];
  });
}

// Event listeners for language switch links
const frLink = document.getElementById("switch-to-fr");
const enLink = document.getElementById("switch-to-en");

frLink.addEventListener("click", () => {
  frLink.classList.add("text-orange");
  enLink.classList.remove("text-orange");

  // Set the selected language to "fr" in localStorage
  setSelectedLanguage("fr");

  // Update the content based on the selected language
  updateContent();
});

enLink.addEventListener("click", () => {
  enLink.classList.add("text-orange");
  frLink.classList.remove("text-orange");

  // Set the selected language to "en" in localStorage
  setSelectedLanguage("en");

  // Update the content based on the selected language
  updateContent();
});

// When the page loads, retrieve the selected language from localStorage and update the content
window.addEventListener("load", () => {
  // Get the selected language from localStorage
  const selectedLanguage = getSelectedLanguage();

  // Set the active class based on the selected language
  if (selectedLanguage === "fr") {
    frLink.classList.add("text-orange");
    enLink.classList.remove("text-orange");
  } else {
    enLink.classList.add("text-orange");
    frLink.classList.remove("text-orange");
  }

  // Update the content based on the selected language
  updateContent();
});
